package com.hanson.mapper;

import java.util.List;
import java.util.Map;

public interface DataCenterMapper {

	
	/**
	 * 商户数据数量查询
	 * @param map
	 * @return
	 */
	public String merchantDataCount(Map<String ,Object> data);
	
	/**
	 * 商户数据查询
	 * @param map
	 * @return
	 */
	public List<Map<String ,Object>> merchantData(Map<String ,Object> data);
	
	/**
	 * 商户数据分析查询
	 * @param map
	 * @return
	 */
	public List<Map<String ,Object>> merchantDataAnalyze(Map<String ,Object> data);
	/**
	 * 商户数据分析数量查询
	 * @param map
	 * @return
	 */
	public String merchantDataAnalyzeCount(Map<String ,Object> data);
	
	
	/**
	 * 当前时间段之前的商户数量
	 * @param map
	 * @return
	 */
	public String oldMerchantNumber(Map<String ,Object> data);
	
	/**
	 * 商户数据分析查询导出excel
	 * @param map
	 * @return
	 */
	public List<Map<String ,Object>> merchantDataAnalyzeExcel(Map<String ,Object> data);
	
	
}
