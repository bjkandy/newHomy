package com.hanson.model.odr;

import java.util.Date;

public class Txis {
	private Integer id;
	private Integer qyid;// 企业id
	private Integer paytype;// 商户提现支付方式（10支付宝 20微支付 30 财务线下打款）
	private Date createdate;// 创建时间
	private Date finishdate;// 结算时间哪天的订单：按日自动结算需要填写此字段，用于将来按日
	private Integer status;// 提现状态:10.未提现 20.提现中 40.成功提现（财务已经线下打
	private Double amount;// 提现总金额
	private Double amountfee;// 提现手续费
	private Double amountreal;// 实际提现金额
	private String tradeno;// 第三方交易号(如果转账走第三方时时需要填写)
	private Integer bdelete;// 是否删除
	private String remark;// 备注
	private String orderid;// 订单id,,分割

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getQyid() {
		return qyid;
	}

	public void setQyid(Integer qyid) {
		this.qyid = qyid;
	}

	public Integer getPaytype() {
		return paytype;
	}

	public void setPaytype(Integer paytype) {
		this.paytype = paytype;
	}

	public Date getCreatedate() {
		return createdate;
	}

	public void setCreatedate(Date createdate) {
		this.createdate = createdate;
	}

	public Date getFinishdate() {
		return finishdate;
	}

	public void setFinishdate(Date finishdate) {
		this.finishdate = finishdate;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public Double getAmount() {
		return amount;
	}

	public void setAmount(Double amount) {
		this.amount = amount;
	}

	public Double getAmountfee() {
		return amountfee;
	}

	public void setAmountfee(Double amountfee) {
		this.amountfee = amountfee;
	}

	public Double getAmountreal() {
		return amountreal;
	}

	public void setAmountreal(Double amountreal) {
		this.amountreal = amountreal;
	}

	public String getTradeno() {
		return tradeno;
	}

	public void setTradeno(String tradeno) {
		this.tradeno = tradeno;
	}

	public Integer getBdelete() {
		return bdelete;
	}

	public void setBdelete(Integer bdelete) {
		this.bdelete = bdelete;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getOrderid() {
		return orderid;
	}

	public void setOrderid(String orderid) {
		this.orderid = orderid;
	}

}
