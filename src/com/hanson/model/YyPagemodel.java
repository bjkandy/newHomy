package com.hanson.model;

import java.io.Serializable;
import java.util.Date;

public class YyPagemodel implements Serializable{
	private static final long serialVersionUID = 1L;
	private Integer id;//主键id
	private String linkurl;//链接地址
	private String pictureurl;//图片名
	private Integer clickCount;//点击次数
	private Integer bdelete;//是否删除,0为已删除,1未删除
	private Date createtime;//创建时间
	private String title;//标题。
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getLinkurl() {
		return linkurl;
	}
	public void setLinkurl(String linkurl) {
		this.linkurl = linkurl;
	}
	public String getPictureurl() {
		return pictureurl;
	}
	public void setPictureurl(String pictureurl) {
		this.pictureurl = pictureurl;
	}
	public Integer getClickCount() {
		return clickCount;
	}
	public void setClickCount(Integer clickCount) {
		this.clickCount = clickCount;
	}
	public Integer getBdelete() {
		return bdelete;
	}
	public void setBdelete(Integer bdelete) {
		this.bdelete = bdelete;
	}
	public Date getCreatetime() {
		return createtime;
	}
	public void setCreatetime(Date createtime) {
		this.createtime = createtime;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
}
